const BUTTON_COLORS = {
  delete: "#F87171",
  reject: "#F87171",
  approve: "#3B82F6",
  primary: "#3B82F6",
  success: "#07B007",
  error: "#EF4444",
  warning: "#F87171",
  suspend: "#F97316",
  edit: "#60A5FA",
};

function CommonButton({
  text,
  onClick,
  variant = "contained",
  size = "small",
  color = "primary",
  icon = null,
  sx = {},
  iconName = null,
}) {
  let finalIcon = icon;
  let finalColor = color;
  const iconSize = getIconSizeFromButtonSize(size);

  if (!icon) {
    if (text === "Delete Ad" || text === "Ban User") {
      finalIcon = getIcon("delete", iconSize);
      finalColor = BUTTON_COLORS.delete;
    } else if (text === "Reject Ad") {
      finalIcon = getIcon("reject", iconSize);
      finalColor = BUTTON_COLORS.reject;
    } else if (text === "Approve Ad" || text === "Approve User") {
      finalIcon = getIcon("approve", iconSize);
      finalColor = BUTTON_COLORS.approve;
    } else if (text === "Suspend User" || text === "Unsuspend User") {
      finalIcon = getIcon("suspend", iconSize);
      finalColor = BUTTON_COLORS.suspend;
    } else if (text === "Edit Ad" || text === "Edit User") {
      finalIcon = getIcon("edit", iconSize);
      finalColor = BUTTON_COLORS.edit;
    } else if (text === "Read All") {
      finalIcon = getIcon("readAll", iconSize);
      finalColor = BUTTON_COLORS.approve;
    } else {
      finalColor = BUTTON_COLORS[color] || color;
    }
  }

  return (
    <Button
      variant={variant}
      size={size}
      onClick={onClick}
      sx={{
        ml: { xs: 0, sm: 2 },
        borderRadius: 3,
        bgcolor: finalColor,
        textTransform: "none",
        minWidth: { xs: iconName || finalIcon ? "auto" : 64, sm: 64 },
        px: { xs: iconName || finalIcon ? 1.5 : 2, sm: 2 },
        ...sx,
      }}
    >
      <Box sx={{ display: "flex", gap: 1 }}>
        {iconName ? getIcon(iconName, iconSize) : finalIcon}
        <Box
          component="span"
          sx={{
            display: {
              xs: iconName || finalIcon ? "none" : "block",
              sm: "block",
            },
          }}
        >
          {text}
        </Box>
      </Box>
    </Button>
  );
}

export default CommonButton;
